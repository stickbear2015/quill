# Extension -> profile lookup built once at import time.
_BY_EXTENSION: dict[str, LanguageProfile] = {
    ext: profile for profile in _PROFILES for ext in profile.extensions
}

PLAIN_PROFILE: LanguageProfile = LanguageProfile("Plain text", ())


def get_profile_for_path(path: Path | None) -> LanguageProfile:
    """Return the language profile for *path*, or the plain-text profile."""
    if path is None:
        return PLAIN_PROFILE
    return _BY_EXTENSION.get(path.suffix.lower(), PLAIN_PROFILE)


def get_profile_by_name(name: str) -> LanguageProfile | None:
    """Return a profile by display name (case-insensitive), or ``None``."""
    name_lower = name.lower()
    for profile in _PROFILES:
        if profile.name.lower() == name_lower:
            return profile
    return None


def all_profiles() -> list[LanguageProfile]:
    """Return all built-in profiles sorted by name."""
    return sorted(_PROFILES, key=lambda p: p.name)
