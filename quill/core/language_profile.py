"""Language profiles for code-aware editing (issue #181).

Each ``LanguageProfile`` describes one coding language: file extensions,
indentation convention, comment syntax, bracket pairs, and a keyword set for
token classification. Profiles are pure data — no ``wx`` imports.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path


